//Execution starts from Main
fun main(){

    // "var" is a Mutable datatype, meaning it's value can be overwritten later
    var myName = "Hemanshu"

    // "println" prints the message and followed by a line separator
    println(myName)

    myName = "Hemanshu Varma"

    // "print" prints only the message
    print(myName)
}